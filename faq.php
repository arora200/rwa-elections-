        <div class="container">
            <h2>Frequently Asked Questions (FAQ) for the Aanchal Vihar Election </h2>
            
            <h3>I. Eligibility to Vote and Member Status</h3>

            <div class="faq-item">
                <h4>1. What is the minimum age requirement to be an eligible voter in the RWA election?</h4>
                <p>A member must be at least **21 years of age** on the date of their admission to the Society to be eligible to vote.</p>
            </div>

            <div class="faq-item">
                <h4>2. Am I eligible to vote if I have outstanding dues owed to the Society?</h4>
                <p>No. Financial default is an **automatic disqualifier** for electoral participation. An individual loses their right to vote if they are in arrears of payment of *any* Society dues, including the annual subscription.</p>
            </div>

            <div class="faq-item">
                <h4>3. How is the "One Flat, One Vote" principle applied in cases of joint ownership?</h4>
                <p>The RWA election operates strictly on the principle of "One Flat, One Vote". In cases of joint ownership of an apartment, **typically only the first owner is stipulated as eligible to vote**.</p>
            </div>

            <div class="faq-item">
                <h4>4. Does the RWA election allow for proxy voting?</h4>
                <p>No, proxy voting is **explicitly prohibited** under the statutory framework. Every eligible member must cast their vote in person.</p>
            </div>

            <div class="faq-item">
                <h4>5. Where can I verify the list of eligible voters?</h4>
                <p>The Governing Body is responsible for preparing and certifying the list of members eligible to vote, known as the Official Register of Voters (Form-XII). This list must be notified or displayed at least 45 days prior to the date scheduled for the General Meeting where the elections will be held.</p>
            </div>

            <h3>II. Candidacy and Nomination</h3>

            <div class="faq-item">
                <h4>6. What specific statutory disqualifications prevent a member from contesting a Governing Body position?</h4>
                <p>A person is statutorily disqualified from contesting if they are declared **insolvent**, deemed to be of **unsound mind**, or have been **convicted of an offence involving moral turpitude** punishable with imprisonment of one year or more.</p>
            </div>

            <div class="faq-item">
                <h4>7. What criteria must a candidate declare regarding their financial standing during nomination?</h4>
                <p>A candidate must formally declare that they are **not in arrears of payment of any Society dues**, including the annual subscription, as financial good standing is mandatory for both voting and candidacy.</p>
            </div>

            <div class="faq-item">
                <h4>8. Are there any additional disqualifications defined by the RWA Bye-laws?</h4>
                <p>Yes, the Society’s registered Bye-laws may introduce reasonable administrative disqualifications. Examples often include being actively engaged in litigation against the RWA or currently undergoing criminal prosecution.</p>
            </div>

            <div class="faq-item">
                <h4>9. Who must propose and second a nomination, and what is their required status?</h4>
                <p>The nomination form requires both a Proposer and a Seconder. Both individuals must be **eligible members in good financial standing** to verify their endorsement.</p>
            </div>

            <div class="faq-item">
                <h4>10. What are the designated posts that can be contested in the election?</h4>
                <p>The designations designated for election must be those defined in the RWA’s registered Bye-laws, typically including core positions such as the President, Secretary, Treasurer, and Executive Members.</p>
            </div>

            <h3>III. Election Structure and Authorities</h3>

            <div class="faq-item">
                <h4>11. What is the maximum tenure for the elected Governing Body?</h4>
                <p>The tenure of the elected members of the Governing Body is statutorily limited and **shall not exceed three years**.</p>
            </div>

            <div class="faq-item">
                <h4>12. Is the RWA mandated to use the Collegium (two-tier) system for elections?</h4>
                <p>The constitution of a Collegium is a mandatory statutory requirement if the **total number of members in the Society is more than 300**. If this threshold is met, the General Body elects the Collegium, and the Collegium then elects the Governing Body.</p>
            </div>

            <div class="faq-item">
                <h4>13. What is the central role of the District Registrar in the election process?</h4>
                <p>The District Registrar (DR) is the mandatory filing authority. The DR receives all required ongoing statutory filings, including the pre-election notice, and the post-election results.</p>
            </div>

            <div class="faq-item">
                <h4>14. What is the authority of the Returning Officer (RO)?</h4>
                <p>The Returning Officer (RO) is the pivotal figure ensuring integrity. The RO possesses the definitive, quasi-judicial authority to **decide any objections concerning the list of eligible members** entitled to vote. The RO's decision on voter eligibility is final for the purposes of that election.</p>
            </div>

            <div class="faq-item">
                <h4>15. If a dispute arises regarding the election outcome, which authority holds the power to adjudicate it?</h4>
                <p>The ultimate authority for adjudicating electoral disputes resides with the **Registrar** (not the District Registrar). The Registrar has the power to hear and decide any doubt or dispute regarding the election or continuance in office of any elected member.</p>
            </div>

            <h3>IV. Timelines and Procedure</h3>

            <div class="faq-item">
                <h4>16. How much notice must be provided to members for the General Body meeting where the elections will be held?</h4>
                <p>A clear notice of **fourteen days (14 days)** must be given to the respective members before the date appointed for the meeting.</p>
            </div>

            <div class="faq-item">
                <h4>17. What is the required quorum for the General Body meeting to hold a valid election?</h4>
                <p>The quorum for a meeting of the General Body shall not be less than **forty percent (40%) of the total members** entitled to vote and present in person, subject to a minimum of four members.</p>
            </div>

            <div class="faq-item">
                <h4>18. Must the government be notified of the election date?</h4>
                <p>Yes. A copy of the 14-day election notice, specifying the date, time, and manner of the election, must be **endorsed to the District Registrar**.</p>
            </div>

            <div class="faq-item">
                <h4>19. What is the deadline for the RWA to file the list of the newly elected Governing Body office-bearers?</h4>
                <p>The list of the newly elected office-bearers of the Governing Body must be filed with the District Registrar within **thirty days (30 days)** of the date the Governing Body elections were held.</p>
            </div>

